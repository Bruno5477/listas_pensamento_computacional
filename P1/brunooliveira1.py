'''
Questão 1

Crie um programa onde o usuário digita o valor_a e o valor_b
a) Realize a soma dos valores
b) Realize a multiplicação dos valores
'''

valor_a = float(input('Insira o valor a: '))
valor_b = float(input('Insira o valor b: '))

soma = valor_a + valor_b
multi = valor_a * valor_b

print(f'a) a soma dos valoes a e b é {soma}')
print(f'b) a multiplicação dos valoes a e b é {multi}')
